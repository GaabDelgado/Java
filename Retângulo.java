package aula_13_07;

public class Retângulo {
	
	    private double base;
	    private double altura;

	    public Retângulo(double base, double altura) {
	        this.base = base;
	        this.altura = altura;
	    }

	    public double getBase() {
	        return base;
	    }

	    public void setBase(double base) {
	        this.base = base;
	    }

	    public double getAltura() {
	        return altura;
	    }

	    public void setAltura(double altura) {
	        this.altura = altura;
	    }
	}


